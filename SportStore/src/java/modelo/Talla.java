
package modelo;

public class Talla {
    private int codigoTalla;
    private String noTalla;

    public Talla() {
    }

    public Talla(int codigoTalla, String noTalla) {
        this.codigoTalla = codigoTalla;
        this.noTalla = noTalla;
    }

    public int getCodigoTalla() {
        return codigoTalla;
    }

    public void setCodigoTalla(int codigoTalla) {
        this.codigoTalla = codigoTalla;
    }

    public String getNoTalla() {
        return noTalla;
    }

    public void setNoTalla(String noTalla) {
        this.noTalla = noTalla;
    }
    
    


    
    
}
